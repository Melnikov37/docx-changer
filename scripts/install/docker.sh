# –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è docxapp –≤ –≥—Ä—É–ø–ø—É docker
if id -u docxapp > /dev/null 2>&1; then
    echo "üë§ –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è docxapp –≤ –≥—Ä—É–ø–ø—É docker..."
    usermod -aG docker docxapp
fi

# –ó–∞–ø—É—Å–∫ Docker
systemctl enable docker
systemctl start docker

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —É—Å—Ç–∞–Ω–æ–≤–∫–∏
echo ""
echo "‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ —É—Å—Ç–∞–Ω–æ–≤–∫–∏ Docker:"
