echo ""
echo "üìù –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ: –î–ª—è –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è –∏–∑–º–µ–Ω–µ–Ω–∏–π –≥—Ä—É–ø–ø—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å docxapp"
echo "   –¥–æ–ª–∂–µ–Ω –≤—ã–π—Ç–∏ –∏ –≤–æ–π—Ç–∏ —Å–Ω–æ–≤–∞, –∏–ª–∏ –≤—ã–ø–æ–ª–Ω–∏—Ç–µ:"
